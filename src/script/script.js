AOS.init();

window.onscroll = function() {scrollFunction()};

function scrollFunction() {
    if (document.body.scrollTop > 80 || document.documentElement.scrollTop > 80) {
        document.getElementById("navbar").style.padding = "-10 0 10px 0";
        document.getElementById("navbar").style.height = "50px";
        document.getElementById("navbar").style.backgroundColor = "rgba(0,0,0,.8)";

    } else {

        document.getElementById("navbar").style.padding = "0";
        document.getElementById("navbar").style.height = "";
        document.getElementById("navbar").style.backgroundColor = "transparent";

    }
} 


